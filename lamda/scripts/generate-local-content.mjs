const groupSizes = ["יחיד", "זוגות", "קבוצה קטנה", "קבוצה בינונית", "קבוצה גדולה"];
const libraryDurations = ["5 דקות", "10 דקות", "15 דקות", "20 דקות", "30 דקות", "45 דקות"];
const objectives = [
  "פתיחה וחיבור לנושא",
  "בניית אמון",
  "העלאת אנרגיה",
  "עיבוד ידע",
  "תרגול מיומנות",
  "שיפור שיתוף פעולה",
  "קבלת החלטות",
  "סיכום והעברה ליישום",
];
const modes = ["online", "in-person", "hybrid"];

function meta(index) {
  return {
    groupSize: groupSizes[index % groupSizes.length],
    duration: libraryDurations[index % libraryDurations.length],
    objective: objectives[index % objectives.length],
    mode: modes[index % modes.length],
  };
}

function makeItems(prefix, count, type, titleFactory, descriptionFactory) {
  return Array.from({ length: count }, (_, index) => ({
    id: `${prefix}-${String(index + 1).padStart(3, "0")}`,
    type,
    title: titleFactory(index),
    description: descriptionFactory(index),
    ...meta(index),
  }));
}

